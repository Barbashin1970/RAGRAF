def gen_data_ttl(reg: Reg) -> str:
    lines = [
        "@prefix :     <http://regulations.local/ontology#> .",
        "@prefix owl:  <http://www.w3.org/2002/07/owl#> .",
        "@prefix rdf:  <http://www.w3.org/1999/02/22-rdf-syntax-ns#> .",
        "@prefix rdfs: <http://www.w3.org/2000/01/rdf-schema#> .",
        "@prefix xsd:  <http://www.w3.org/2001/XMLSchema#> .",
        "",
        f"# {reg.name}",
        f"# Роль: {reg.role}",
        f"# Источник: {reg.source}",
        "",
        ":Regulation a owl:Class .",
        "",
        ":name           a owl:DatatypeProperty ; rdfs:domain :Regulation ; rdfs:range xsd:string .",
        ":date           a owl:DatatypeProperty ; rdfs:domain :Regulation ; rdfs:range xsd:date .",
        ":recommendation a owl:DatatypeProperty ; rdfs:domain :Regulation ; rdfs:range xsd:string .",
        "",
    ]
    # OWL property declarations
    for p in reg.params:
        lines.append(
            f":{p.name}          a owl:DatatypeProperty ; rdfs:domain :Regulation ; rdfs:range xsd:decimal ."
        )
        lines.append(
            f":{p.name}Deviation a owl:DatatypeProperty ; rdfs:domain :Regulation ; rdfs:range xsd:decimal ."
        )
    lines.append("")
    lines.append(f":{reg.pascal}")
    lines.append(f"    a                      :Regulation ;")
    lines.append(f"    :name                  {json.dumps(reg.name, ensure_ascii=False)} ;")
    lines.append(f"    :date                  \"{reg.date}\"^^xsd:date ;")
    for p in reg.params:
        lines.append(f"    :{p.name}              {p.ref} ;")
        lines.append(f"    :{p.name}Deviation     {p.dev} ;")
    rec_quoted = json.dumps(reg.recommendation, ensure_ascii=False)
    lines.append(f"    :recommendation        {rec_quoted} .")
    return "\n".join(lines) + "\n"


def gen_shapes_ttl(reg: Reg) -> str:
    lines = [
        "@prefix :    <http://regulations.local/ontology#> .",
        "@prefix sh:  <http://www.w3.org/ns/shacl#> .",
        "@prefix xsd: <http://www.w3.org/2001/XMLSchema#> .",
        "",
        f":{reg.pascal.replace('Regulation', 'Shape')}",
        "    a sh:NodeShape ;",
        "    sh:targetClass :Regulation ;",
        "",
        "    sh:property [ sh:path :name ; sh:datatype xsd:string ; sh:minCount 1 ] ;",
        "    sh:property [ sh:path :date ; sh:datatype xsd:date ; sh:minCount 1 ] ;",
    ]
    for i, p in enumerate(reg.params):
        last = i == len(reg.params) - 1
        sep = " ." if last else " ;"
        lines.append("")
        lines.append(f"    sh:property [")
        lines.append(f"        sh:path         :{p.name} ;")
        lines.append(f"        sh:datatype     xsd:decimal ;")
        lines.append(f"        sh:minCount     1 ;")
        lines.append(f"        sh:minInclusive {p.min_inclusive} ;")
        lines.append(f"        sh:maxInclusive {p.max_inclusive} ;")
        comment = json.dumps(p.comment, ensure_ascii=False)
        lines.append(f"        sh:message      {comment}@ru")
        lines.append(f"    ] ;")
        lines.append(f"    sh:property [")
        lines.append(f"        sh:path         :{p.name}Deviation ;")
        lines.append(f"        sh:datatype     xsd:decimal ;")
        lines.append(f"        sh:minCount     1 ;")
        lines.append(f"        sh:minInclusive 0.0 ;")
        lines.append(f"        sh:maxInclusive {max(p.max_inclusive * 0.5, p.dev * 5)}")
        lines.append(f"    ]{sep}")
    return "\n".join(lines) + "\n"


